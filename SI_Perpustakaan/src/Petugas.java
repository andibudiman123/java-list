    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        try{
            Statement stm = konekDB.createStatement();
            
            ResultSet tampil = stm.executeQuery("select * from petugas order by nama ASC");
            
            tabel.getDataVector().removeAllElements();
            tabel.fireTableDataChanged();
            
            while(tampil.next()){
                tabel.addRow(new Object[]
                {tampil.getString("kode_petugas"),
                tampil.getString("nama"),
                tampil.getString("telpon")});
            }
            
            tabelPetugas.setModel(tabel);
        }catch (Exception e){
        }
    }//GEN-LAST:event_formWindowActivated

