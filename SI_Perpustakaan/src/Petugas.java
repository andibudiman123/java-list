    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        if((tkode.getText().isEmpty()) || (tNama.getText().isEmpty()) || (tTelp.getText().isEmpty()) || (tpass.getText().isEmpty())){
          JOptionPane.showMessageDialog(this,"Ada Inputan Kosong", "Info", JOptionPane.INFORMATION_MESSAGE);
        }else{
        try{
            PreparedStatement stm = konekDB.prepareStatement("insert into petugas(kode_petugas, nama, telpon, password)" + " values('"+ tkode.getText() +"','"+ tNama.getText() +"','"+ tTelp.getText() +"','"+ tpass.getText() +"')");
            stm.executeUpdate();
            bersih();
            tkode.requestFocus();
        } catch (Exception e){
        }
        }
    }//GEN-LAST:event_btSimpanActionPerformed

    private void tkodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tkodeKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(tkode.getText().isEmpty()){
                JOptionPane.showMessageDialog(this,"Kode Tidak boleh Kosong","Info", JOptionPane.INFORMATION_MESSAGE);
            }else if(tkode.getText().length() > 8){
                JOptionPane.showMessageDialog(this,"Kode Tidak boleh Lebih dari 8 karakter","Info", JOptionPane.INFORMATION_MESSAGE);
                tkode.setText("");
            }else{
            try{
                Statement stm = konekDB.createStatement();
                String data = "select * from petugas where kode_petugas ='"+ tkode.getText() +"'";
                
                ResultSet ada = stm.executeQuery(data);
                
                if(ada.next()){
                    tNama.setText(ada.getString("nama"));
                    tTelp.setText(ada.getString("telpon"));
                    tpass.setText(ada.getString("password"));
                }else{
                    tNama.setText("");
                    tTelp.setText("");
                    tpass.setText("");
                    
                    tNama.requestFocus();
                }                
            }catch (Exception e){
            }
            }
            
        }
    }//GEN-LAST:event_tkodeKeyPressed

